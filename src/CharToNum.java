

public class CharToNum {

	public int charToNumber1(int start) {
		switch(start) {
		case (65) :
			start = 1;
		break;
		case (66) :
			start = 2;
		break;
		case (67) :
			start = 3;
		break;
		case (68) :
			start = 4;
		break;
		case (69) :
			start = 5;
		break;
		}
		return start;
	}
		
		


	public int charToNumber2(int end) {
		switch(end) {
		case (65) :
			end = 1;
		break;
		case (66) :
			end = 2;
		break;
		case (67) :
			end = 3;
		break;
		case (68) :
			end = 4;
		break;
		case (69) :
			end = 5;
		break;
		}
		return end;
	}

}
